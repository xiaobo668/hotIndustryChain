function chainNames(key) {
  const d = INDUSTRY_DATA[key];
  const set = new Set();
  ['upstream', 'midstream', 'downstream'].forEach((t) => {
    (d[t] || []).forEach((seg) => {
      (seg.companies || []).forEach((co) => set.add(co.name));
    });
  });
  return set;
}

