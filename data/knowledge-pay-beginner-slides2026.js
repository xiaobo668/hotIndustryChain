function mergeKnowledgePayBeginnerSlides() {
  if (typeof KNOWLEDGE_PAY2026 === 'undefined' || !KNOWLEDGE_PAY_BEGINNER_SLIDES) return;
  var course = KNOWLEDGE_PAY2026.courses && KNOWLEDGE_PAY2026.courses[0];
  if (!course || !course.chapters) return;

  course.chapters.forEach(function (ch) {
    var extra = KNOWLEDGE_PAY_BEGINNER_SLIDES[ch.id];
    if (extra && extra.length) {
      ch.slides = ch.slides.concat(extra);
    }
  });

  var total = 0;
  course.chapters.forEach(function (ch) {
    total += (ch.slides || []).length;
  });
  course.slideCount = total;
}

if (typeof window !== 'undefined') {
  window.KNOWLEDGE_PAY_BEGINNER_SLIDES = KNOWLEDGE_PAY_BEGINNER_SLIDES;
  window.mergeKnowledgePayBeginnerSlides = mergeKnowledgePayBeginnerSlides;
}
